#!/usr/bin/env python3
"""
Create a two-panel Sentinel-2 figure:

    Image (A): single-date cloud-masked Sentinel-2 RGB image
    Image (B): Oct-Dec median composite Sentinel-2 RGB image

Image (A) should be generated by:
    scripts/download_single_scene/download_s2_single_date_rgb_2023.py

Expected composite band paths:
    data/raw/sentinel_gemini/BD_COASTAL_BBOX/S2_2023_octdec_B4_mosaic_10m_lzw.tif
    data/raw/sentinel_gemini/BD_COASTAL_BBOX/S2_2023_octdec_B3_mosaic_10m_lzw.tif
    data/raw/sentinel_gemini/BD_COASTAL_BBOX/S2_2023_octdec_B2_mosaic_10m_lzw.tif

Example run using the bbox from the map:
    python scripts/visualization/make_s2_single_vs_composite_map.py \
        --single-rgb data/raw/sentinel_single_scene/s2_2023_10_18_single_date_rgb.tif \
        --bbox 90.20 22.30 90.283333 22.366667 \
        --year 2023 \
        --out outputs/figures/compare_single_vs_composite_s2_2023.png

BBOX order:
    min_lon min_lat max_lon max_lat

Approximate bbox explanation:
    90°12′E = 90.20
    90°17′E = 90.283333
    22°18′N = 22.30
    22°22′N = 22.366667

Reproduction and AOI adaptation
-------------------------------
Workflow role: Turn prepared rasters, vectors, and tables into thesis-ready figures.

Run commands from the repository root after activating the project environment and
installing ``requirements.txt``. Keep immutable raw inputs separate from generated
intermediate and output products, and create a new output directory for each AOI/run.

Interface and data contract
~~~~~~~~~~~~~~~~~~~~~~~~~~~
The command-line interface exposes ``--single-rgb``, ``--comp-dir``, ``--year``, ``--comp-red``, ``--comp-green``, ``--comp-blue``, ``--bbox``, ``--out``, ``--dpi``, ``--nodata``, ``--title-a``, ``--title-b``, ``--north-arrow``, ``--missing-color``, ``--stretch-low``, ``--stretch-high``. Run the ``--help`` command below for required values, defaults, and accepted choices.
Inputs must exist before execution. Outputs are written to the CLI destinations or
to the path constants/defaults documented above and in the parser. Preserve CRS,
transform, resolution, nodata, band/feature order, and class IDs between dependent
stages; those properties are part of the analytical data contract.

Adapting to another area of interest
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
Replace raster/vector/palette paths with target-AOI products and verify matching CRS, extent, class IDs, units, and map annotations before publication.
Record the replacement AOI, acquisition dates, CRS, resolution, class mapping, random
seed, and software environment. Validate intermediate dimensions/statistics and inspect
final maps or tables before using them in analysis or publication.
"""

